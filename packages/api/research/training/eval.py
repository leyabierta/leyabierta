#!/usr/bin/env python3
"""
Evaluate a cross-encoder reranker against the Ley Abierta eval-v2 holdout.

Reports R@1, R@5, R@10 — overall AND broken down per register
(formal / informal / procedural). The per-register breakdown is the gate
we set in the Fase 1a register-coverage decision: if training on a more
balanced query mix causes informal R@10 to regress, we want to see it
*here*, not after deploy.

Two evaluation modes:

    --mode candidates
        Read precomputed retrieval candidates (top-K from the current
        BM25/vector pipeline) and rerank them. The "current candidates"
        file is generated by `bun run packages/api/research/dump-eval-candidates.ts`
        — a TS-side helper that lives next to the rest of the retrieval
        code and runs the same pipeline the API serves.

    --mode oracle
        Skip retrieval; assume the gold articles are already in the
        candidate pool (passed in via the candidates file). Useful for
        isolating reranker quality from retrieval noise.

Reranker selection:

    --reranker base:BAAI/bge-reranker-v2-m3
        Use the base cross-encoder with no LoRA (a from-scratch baseline).

    --reranker lora:packages/api/research/training/adapters/bge-v1
        Load the LoRA adapter on top of the base model recorded in
        train_config.json.

    --reranker none
        No reranking — report retrieval-only R@k.

Output:

    A JSON report at --report path with the following shape:
        {
          "overall": {"r@1": ..., "r@5": ..., "r@10": ...},
          "per_register": {
            "informal": {"r@1": ..., "r@5": ..., "r@10": ..., "n": ...},
            ...
          },
          "config": {...},
          "timestamp": "..."
        }

Status: scaffolding. The candidates-dump helper does not exist yet (see
`dump-eval-candidates.ts` TODO at the end of this file). Until it lands,
this script will refuse to run in `candidates` mode. `oracle` mode works
once you supply a candidates JSONL.
"""

from __future__ import annotations

import argparse
import datetime as dt
import json
import sys
from dataclasses import asdict, dataclass
from pathlib import Path
from typing import Iterable, Literal


@dataclass
class EvalConfig:
    eval_path: Path
    candidates_path: Path | None
    reranker_spec: str
    mode: Literal["candidates", "oracle"]
    report_path: Path
    top_k: int
    register_field: str


def load_eval(path: Path) -> list[dict]:
    """eval-v2.json has shape {description, version, results: [...]}."""
    raw = json.loads(path.read_text(encoding="utf-8"))
    items = raw.get("results", raw)
    return list(items)


def load_candidates(path: Path) -> dict[int, list[dict]]:
    """Each line is one eval question's candidates list:

    {"question_id": 7, "candidates": [{"norm_id": "...", "block_id": "...", "score": ...}, ...]}

    Returns: question_id → list of candidates (length up to top-K).
    """
    out: dict[int, list[dict]] = {}
    with path.open("r", encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            row = json.loads(line)
            out[int(row["question_id"])] = list(row["candidates"])
    return out


def load_reranker(spec: str):
    """Load the reranker from a `kind:identifier` spec. Lazy import so the
    --help path doesn't require torch."""
    if spec == "none":
        return None

    # max_length cap on tokens — bge-reranker-v2-m3 defaults to 8192 which
    # OOMs on MPS with even modest batches. 512 is the cross-encoder norm
    # and matches what the reranker pipeline uses in production.
    MAX_LENGTH = 512

    if spec.startswith("base:"):
        from sentence_transformers import CrossEncoder

        model_name = spec[len("base:") :]
        return CrossEncoder(model_name, num_labels=1, max_length=MAX_LENGTH)

    if spec.startswith("model:") or spec.startswith("ft:"):
        # Full fine-tuned CrossEncoder saved by sentence_transformers.fit().
        from sentence_transformers import CrossEncoder

        prefix = "model:" if spec.startswith("model:") else "ft:"
        model_dir = spec[len(prefix) :]
        return CrossEncoder(model_dir, num_labels=1, max_length=MAX_LENGTH)

    if spec.startswith("lora:"):
        adapter_dir = Path(spec[len("lora:") :])
        config_path = adapter_dir / "train_config.json"
        if not config_path.exists():
            raise FileNotFoundError(
                f"--reranker lora:{adapter_dir} but no train_config.json there"
            )
        cfg = json.loads(config_path.read_text(encoding="utf-8"))
        from peft import PeftModel
        from sentence_transformers import CrossEncoder

        ce = CrossEncoder(cfg["base_model"], num_labels=1, max_length=MAX_LENGTH)
        ce.model = PeftModel.from_pretrained(ce.model, str(adapter_dir / "lora_adapter"))
        ce.model.eval()
        return ce

    raise ValueError(f"unknown reranker spec: {spec!r}")


def rerank(reranker, query: str, candidates: list[dict]) -> list[dict]:
    """Score (query, candidate.text) pairs and return candidates sorted by
    score descending. Each candidate must have a "text" field; if missing,
    it stays at the end with score=-inf so retrieval order is preserved."""
    if reranker is None or not candidates:
        return list(candidates)
    pairs = []
    indices = []
    for i, c in enumerate(candidates):
        text = c.get("text")
        if text:
            pairs.append([query, text])
            indices.append(i)
    if not pairs:
        return list(candidates)
    # batch_size=8: keeps per-step MPS memory below ~3 GB on M4 Max even
    # with 512-token sequences. Default of 32 OOMs with bge-reranker-v2-m3.
    scores = reranker.predict(pairs, batch_size=8, show_progress_bar=False)
    scored = list(candidates)
    for idx, score in zip(indices, scores):
        scored[idx] = {**scored[idx], "_rerank_score": float(score)}
    # Items without text fall to the bottom.
    scored.sort(
        key=lambda c: c.get("_rerank_score", float("-inf")),
        reverse=True,
    )
    return scored


def hit_at_k(
    candidates: Iterable[dict], expected_norms: Iterable[str], k: int
) -> bool:
    """A "hit" is any expected norm appearing in the top-K by *norm_id*.
    Article-level matching is too strict for the 0.5/0.76/0.87 baseline
    we already published; this lines up with the existing eval-gate.ts."""
    expected = set(expected_norms)
    for c in list(candidates)[:k]:
        if c.get("norm_id") in expected:
            return True
    return False


def evaluate(cfg: EvalConfig) -> dict:
    eval_items = load_eval(cfg.eval_path)
    if cfg.candidates_path is None:
        raise ValueError("candidates file is required (oracle and candidates modes)")
    cands_by_q = load_candidates(cfg.candidates_path)

    reranker = load_reranker(cfg.reranker_spec)

    # Aggregate by overall + per register. Items without a register tag
    # (legacy or out-of-scope adversarial) bucket as "untagged".
    counters: dict[str, dict[str, int]] = {
        bucket: {"n": 0, "r1": 0, "r5": 0, "r10": 0}
        for bucket in ("overall", "formal", "informal", "procedural", "untagged")
    }
    in_scope_total = sum(
        1
        for q in eval_items
        if (q.get("expectedNorms") or []) and int(q["id"]) in cands_by_q
    )
    progressed = 0
    print(
        f"[eval.py] grading {in_scope_total} in-scope questions"
        f" with reranker={cfg.reranker_spec}",
        flush=True,
    )
    import time as _time

    started = _time.time()

    for q in eval_items:
        qid = int(q["id"])
        expected = q.get("expectedNorms") or []
        # Out-of-scope questions (empty expectedNorms) aren't graded.
        if not expected:
            continue
        # Skip if we have no candidates for this question — better to
        # report partial coverage than silently zero-fill.
        if qid not in cands_by_q:
            continue

        register = q.get(cfg.register_field, "untagged")
        bucket = register if register in counters else "untagged"

        ranked = rerank(reranker, q["question"], cands_by_q[qid])
        for k, key in ((1, "r1"), (5, "r5"), (10, "r10")):
            if hit_at_k(ranked, expected, k):
                counters[bucket][key] += 1
                counters["overall"][key] += 1
        counters[bucket]["n"] += 1
        counters["overall"]["n"] += 1

    def to_metrics(c: dict[str, int]) -> dict[str, float | int]:
        n = c["n"] or 1  # avoid div by zero in serialization; reported "n" stays accurate
        return {
            "n": c["n"],
            "r@1": round(c["r1"] / n, 4),
            "r@5": round(c["r5"] / n, 4),
            "r@10": round(c["r10"] / n, 4),
        }

    report = {
        "overall": to_metrics(counters["overall"]),
        "per_register": {
            r: to_metrics(counters[r]) for r in ("formal", "informal", "procedural", "untagged")
        },
        "config": asdict(cfg) | {"eval_path": str(cfg.eval_path), "candidates_path": str(cfg.candidates_path) if cfg.candidates_path else None, "report_path": str(cfg.report_path)},
        "timestamp": dt.datetime.utcnow().isoformat() + "Z",
    }
    return report


def parse_args(argv: list[str]) -> EvalConfig:
    p = argparse.ArgumentParser(
        description=__doc__,
        formatter_class=argparse.RawDescriptionHelpFormatter,
    )
    p.add_argument("--eval", required=True, type=Path, dest="eval_path")
    p.add_argument("--candidates", type=Path, default=None)
    p.add_argument(
        "--reranker",
        default="none",
        help='reranker spec: "none", "base:<model-id>", or "lora:<adapter-dir>"',
    )
    p.add_argument(
        "--mode",
        choices=("candidates", "oracle"),
        default="candidates",
    )
    p.add_argument("--report", required=True, type=Path, dest="report_path")
    p.add_argument("--top-k", type=int, default=80)
    p.add_argument(
        "--register-field",
        default="register",
        help='which JSON field to use for register tags (default "register"; use "register_heuristic" for the heuristic interim tagging)',
    )
    args = p.parse_args(argv)
    return EvalConfig(
        eval_path=args.eval_path,
        candidates_path=args.candidates,
        reranker_spec=args.reranker,
        mode=args.mode,
        report_path=args.report_path,
        top_k=args.top_k,
        register_field=args.register_field,
    )


if __name__ == "__main__":
    cfg = parse_args(sys.argv[1:])
    if cfg.mode == "candidates" and cfg.candidates_path is None:
        print(
            "[eval.py] error: --candidates is required in candidates mode."
            " Generate it via: bun run packages/api/research/dump-eval-candidates.ts"
        )
        sys.exit(2)
    report = evaluate(cfg)
    cfg.report_path.parent.mkdir(parents=True, exist_ok=True)
    cfg.report_path.write_text(
        json.dumps(report, indent=2, ensure_ascii=False) + "\n",
        encoding="utf-8",
    )
    overall = report["overall"]
    print(
        f"[eval.py] overall n={overall['n']}"
        f" r@1={overall['r@1']:.3f}"
        f" r@5={overall['r@5']:.3f}"
        f" r@10={overall['r@10']:.3f}"
    )
    for r in ("informal", "formal", "procedural"):
        m = report["per_register"][r]
        print(
            f"          {r:<10} n={m['n']:>3}"
            f" r@1={m['r@1']:.3f} r@5={m['r@5']:.3f} r@10={m['r@10']:.3f}"
        )
    print(f"[eval.py] report written to {cfg.report_path}")
